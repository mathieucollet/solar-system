<template>
  <div class="container">
    <div v-for="planet in planets" :key="planet.id">{{ planet.id }}</div>
  </div>
</template>

<script>
import Planet from '~/models/Planet'

export default {
  name: 'Index',
  async asyncData() {
    const response = await Planet.orderBy('id').first()
    const planets = response.bodies
    return { planets }
  },
}
</script>

